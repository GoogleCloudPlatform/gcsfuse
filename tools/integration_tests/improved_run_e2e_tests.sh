# filter_array: Filters an array in place keeping only elements matching the regex.
# Supports '!' prefix to invert the match (exclude).
# Args: $1 = name of the array variable, $2 = regex pattern.
filter_array() {
  local -n arr=$1
  local regex=$2

  # Return early if regex or array is empty
  [[ -z "$regex" || ${#arr[@]} -eq 0 ]] && return

  # Check if regex starts with '!', meaning we want to exclude matches
  local invert=""
  if [[ "$regex" == !* ]]; then
    invert="-v"
    regex="${regex#!}" # Strip the '!' prefix
  fi

  # Filter the array using grep and map the results back to the array
  mapfile -t arr < <(printf '%s\n' "${arr[@]}" | grep $invert -E "$regex")
}

