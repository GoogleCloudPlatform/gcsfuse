# Exit on error, treat unset variables as errors, and propagate pipeline errors.
set -euo pipefail

# This script is used to collect the resource(mem,cpu,disk) usage of the machine in the file at given interval.
# This script can also be in use to print the resource usage visualisation in graph format.
usage() {
    echo "A script to collect and visualize system resource usage."
    echo ""
    echo "========================================================================"
    echo ""
    echo "COMMAND: COLLECT"
    echo ""
    echo "  Gathers resource usage (CPU, memory, disk) at predefined intervals."
    echo ""
    echo "  USAGE:"
    echo "    $0 COLLECT <FILE_PATH>"
    echo ""
    echo "  ARGUMENTS:"
    echo ""
    echo "    <FILE_PATH>   The path to the output file where resource data will be"
    echo "                  saved. The script will create or overwrite this file."
    echo ""
    echo "========================================================================"
    echo ""
    echo "COMMAND: PRINT"
    echo ""
    echo "  Visualizes resource data from a file in a graph format."
    echo ""
    echo "  USAGE:"
    echo "    $0 PRINT <FILE_PATH>"
    echo ""
    echo "  ARGUMENTS:"
    echo "    <FILE_PATH>   The path to the input data file (generated by COLLECT)"
    echo "                  that contains the statistics to be visualized."
    echo ""
    echo "========================================================================"
    exit 1
}

readonly USAGE_COLLECTION_INTERVAL_SECONDS=10          # Collect usage every 10s.
readonly USAGE_COLLECTION_DURATION_SECONDS=$((3 * 60 * 60)) # Auto collect usage for upto 3 hrs only (VM timeout is 3 hrs in kokoro).
readonly COLLECT="COLLECT"
readonly PRINT="PRINT"

# Validate number of arguments.
if [[ $# -ne 2 ]]; then
    echo "Error: Invalid number of arguments."
    usage
fi

readonly COMMAND="$1"
readonly FILE_PATH="$2"

# Helper function to get CPU Usage
# Outputs: CPU Usage percentage, or 0 on failure.
get_cpu_usage() {
    local usage
    # Attempt to get usage and store it
    usage=$(top -bn1 | awk '/^%Cpu/ {printf "%.0f\n", 100 - $8}')
    # Echo the usage variable; if it's empty, echo "0" instead
    echo "${usage:-0}"
}

# Helper function to get Memory Usage
# Outputs: Memory Usage percentage, or 0 on failure.
get_mem_usage() {
    local mem_usage
    mem_usage=$(free | awk '/^Mem:/ {printf "%.0f", $3/$2 * 100}')
    echo "${mem_usage:-0}"
}

# Helper function to get Disk Usage for / (root)
# Outputs: Disk Usage percentage, or 0 on failure.
get_disk_usage() {
    local disk_usage
    disk_usage=$(df -P / | awk 'END{printf "%.0f", $5}')
    echo "${disk_usage:-0}"
}

# Helper function to collect the usage and write to file.
# Output: Appends new line to file with %cpu %mem %disk at given interval.
collect_all_metric() {
    printf "CPU:\nMEM:\nDISK:\n" >"$FILE_PATH"
    while true; do
        if [ "$SECONDS" -ge "$USAGE_COLLECTION_DURATION_SECONDS" ]; then
            echo "Exiting due to monitoring time limit."
            exit 0
        fi
        cpu=$(get_cpu_usage)
        mem=$(get_mem_usage)
        disk=$(get_disk_usage)

        # Use 'sed -i' to perform an in-place edit of the file.
        # For each metric, it finds the correct line and appends the new value.
        sed -i -e "s/^CPU:.*/& ${cpu}/" \
            -e "s/^MEM:.*/& ${mem}/" \
            -e "s/^DISK:.*/& ${disk}/" \
            "$FILE_PATH"

        sleep "$USAGE_COLLECTION_INTERVAL_SECONDS"
    done
}

# Helper function to print usage of single metric in graph format.
print_metric_in_graph_format() {
    local line="$1"
    local label numbers
    label=$(echo "$line" | cut -d: -f1)
    numbers=$(echo "$line" | cut -d: -f2)
    # Read the space-separated numbers into a bash array
    local -a values
    read -r -a values <<<"$numbers"
    echo ""
    echo "$label usage in percentage every $USAGE_COLLECTION_INTERVAL_SECONDS seconds:"
    echo ""
    max_value=$(printf "%s\n" "${values[@]}" | sort -n | tail -n 1)
    max_value=$(( (max_value + 5 > 100) ? 100 : max_value + 5 )) # Add some buffer upwards from max usage.
    # Make graph for each value
    for ((i = max_value; i >= 0; i--)); do
        printf "%s@ %3d%%|" "$label" "$i"
        for val in "${values[@]}"; do
            if [[ $val -ge $i ]]; then
                echo -n "*"
            else
                echo -n " "
            fi
        done
        echo ""
    done
    SEPARATOR_LINE=$(printf '=%.0s' $(seq 1 $(( ${#values[@]} + 15 )) )) # data points + 15 more for to cover label and %.
    echo "$SEPARATOR_LINE"
}

# Helper function to print usage of all metrics.
print_each_metric() {
    while read -r line || [ -n "$line" ]; do
        print_metric_in_graph_format "$line"
    done <"$FILE_PATH"
}

if [[ "$COMMAND" == "$COLLECT" ]]; then
    collect_all_metric
elif [[ "$1" == "$PRINT" ]]; then
    print_each_metric
else
    echo "Error: Invalid command."
    usage
fi
